x = int(input("What's x? \n"))
y = int(input("What's y? \n"))

if x < y :
  print("x is less than y")
elif x > y :
  print("y is less than x")
else :
  print("x is equal to y")
  
  
if x > y or y > x :
  print("x is not equal to y")
else :
  print("x is equal to y")
  
  
if x == y :
  print("x is equal to y")
else :
  print("x is not equal to y")
  
  
  
  
    
